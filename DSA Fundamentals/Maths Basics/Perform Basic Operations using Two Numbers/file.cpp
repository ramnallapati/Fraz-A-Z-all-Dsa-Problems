
#include<iostream>
using namespace std;

int main() {

  int num1;
  cout<<"Enter first value :  \n";
  cin>>num1;

  int num2;
  cout<<"Enter second value : \n";
  cin>>num2;

  cout<<"Addition of Two numbers : \n";
  cout<<num1 << " + "<<num2 << " is : "<<num1 + num2<<endl;

  cout<<"Subtraction of Two numbers : \n";
  cout<<num1 << " - "<<num2 << " is : "<<num1 - num2<<endl;

  cout<<"Multiplication of Two numbers : \n";
  cout<<num1 << " * "<<num2 << " is : "<<num1 * num2<<endl;


  cout<<"Division of Two numbers : \n";
  cout<<num1 << " / "<<num2 << " is : "<<num1 / num2<<endl;


}